//Example 1: Simple Number Pattern (Increasing Sequence)

#include <iostream>
using namespace std;

int main() {
    // Loop from 1 to 5
    for (int i = 1; i <= 5; i++) {
        // Print the current number
        cout << i << " ";
    }
    cout << endl; // Move to the next line after the loop
    return 0;
}
