//1. Arithmetic Operators
#include <iostream>
using namespace std;

int main() {
    int a = 10, b = 20;
    cout << "Addition: " << (a + b) << endl;
    cout << "Subtraction: " << (a - b) << endl;
    cout << "Multiplication: " << (a * b) << endl;
    cout << "Division: " << (b / a) << endl;
    cout << "Modulus: " << (b % a) << endl;
    return 0;
}