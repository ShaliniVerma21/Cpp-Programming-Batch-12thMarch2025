// 3. Concatenation of Two Strings
//   - Concatenation is the process of combining two or more strings into a single string.


#include <iostream>
#include <string>
using namespace std;

int main() {
    string first = "Good";
    string second = "Morning";
    string result = first + " " + second;
    cout << "Combined: " << result << endl;
    return 0;
}
